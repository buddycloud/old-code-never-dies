package com.buddycloud.pubsub.log;

import org.apache.log4j.Logger;


public class LogMe {
	static final Logger logger = Logger.getLogger("Broadcaster");
	
	public static void warning(String line) {
		logger.warn(line);
	}
	public static void info(String line) {
		logger.info(line);
	}
	public static void debug(String line) {
		logger.debug(line);
	}
}
