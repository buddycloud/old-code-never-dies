package com.buddycloud.pubsub.packetHandlers.IQ.Namespace;

import org.dom4j.Element;
import org.xmpp.packet.IQ;

public class QueuedIQAction {
	
	private IQ iq;
	private Element e;
	
	public QueuedIQAction(IQ iq, Element e) {
		this.iq = iq;
		this.e = e;
	}
	
	public IQ getIQ() {
		return this.iq;
	}
	
	public Element getElelement() {
		return this.e;
	}
}
